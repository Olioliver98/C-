#include <iostream>
using namespace std;

int main(int argc, char const *argv[])
{
    // declaracion de arrelo unidimencional
    int numeros[5];

    //asinacion de valores al arreglo
    numeros[0] = 10;
    numeros[1] = 5;
    numeros[2] = 7;
    numeros[3] = 50;
    numeros[4] = 2;

    //Lectura de arreglo
    cout<< numeros[0] <<endl;
    cout<< numeros[1] <<endl;
    cout<< numeros[2] <<endl;
    cout<< numeros[3] <<endl;
    cout<< numeros[4] <<endl;

    return 0;
}
